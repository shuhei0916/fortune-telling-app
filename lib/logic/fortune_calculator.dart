  /// 生年月日から動物キャラのID(1-60)を計算する
  static int? calculateAnimalId(DateTime birthday) {
    final year = birthday.year;
    final month = birthday.month;
    final day = birthday.day;

    if (_animalNumberTable.containsKey(year)) {
      final monthTable = _animalNumberTable[year]!;
      if (monthTable.containsKey(month)) {
        final baseNumber = monthTable[month]!;
        int result = baseNumber + day;
        
        // サイトのルール: 「60以上になったときは、60を引いて計算します」
        // これは、1-60の範囲に収めるための剰余演算に似ているが、少し違う。
        // 例: 59+1=60 -> 60, 59+2=61 -> 1
        // (baseNumber + day - 1) % 60 + 1 で実現できる
        return (baseNumber + day - 1) % 60 + 1;
      }
    }
    // 対応表に年/月がない場合はnullを返す
    return null;
  }
